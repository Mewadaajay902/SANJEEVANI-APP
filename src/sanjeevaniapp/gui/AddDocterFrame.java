    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
if(validateInput()==false)
{
    JOptionPane.showMessageDialog(null, "please fill all the fields","Empty values !",JOptionPane.ERROR_MESSAGE);
return; 
}
try
{
    String id=jcbEmpId.getSelectedItem().toString();
    String userId=txtUserId.getText();
    String docid=txtDocId.getText();
    String qualification=txtQualification.getText();
    String specialist=txtSpecialist.getText();
    char[] pwdd=pwd.getPassword();
    String pwddd=String.valueOf(pwdd);
    char[] repwd1=repw.getPassword();
    String repwdd=String.valueOf(repwd1);
    if(!passwordMatch(pwddd,repwdd))
    {
       JOptionPane.showMessageDialog(null, "Password doesnot match","Empty values !",JOptionPane.ERROR_MESSAGE);
return; 
    }
    DocterPojo user=new DocterPojo();
    user.setUserid(userId);
    user.setEmpname(txtEmpName.getText());
    user.setEmpid(id);
    user.setPassword(pwddd);
    user.setUserType("DOCTER");
    
    DocterPojo userr=new DocterPojo();
    userr.setUserid(userId); 
    userr.setDocid(docid);
    userr.setQualification(qualification);
    userr.setSpecialist(specialist);
    
    
    boolean result=DocterDao.addDocter(user);
    boolean result1=DocterDao.addinDocter(userr);
    if(result)
    {
       JOptionPane.showMessageDialog(null, "record successfully added in users","success",JOptionPane.INFORMATION_MESSAGE);
    //clearText();
    //loadReceptionistDetails();
    }
    if(result1)
    {
       JOptionPane.showMessageDialog(null, "record successfully added in docter table too","success",JOptionPane.INFORMATION_MESSAGE);
    //clearText();
    //loadReceptionistDetails();
    }
    else
        JOptionPane.showMessageDialog(null, "Could not add receptionist ","Error",JOptionPane.ERROR_MESSAGE);

    
}
catch(SQLException exx)
            {
            JOptionPane.showMessageDialog(null,"DBError"+exx,"Empty values",JOptionPane.ERROR_MESSAGE);
            exx.printStackTrace();
            }


        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

