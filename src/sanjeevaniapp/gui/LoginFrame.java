    private boolean validateInput()
    {
       userId=txtUserId .getText();
       userId=userId.trim();
       char[] pwd=txtPassword.getPassword();
       
       if(userId.isEmpty()|| pwd.length==0)
           return false;
       else
       {
           password=String.valueOf(pwd);
           password=password.trim();
           return true;
       }
           
    }
    
    private String getUserType()
    {
        if(jrbAdminOptionsFrame.isSelected())
            return jrbAdminOptionsFrame.getText().toUpperCase();
        else if(jrbRecieptionistOptionsFrame.isSelected())
            return jrbRecieptionistOptionsFrame.getText().toUpperCase();
        else if(jrbDocterOptionsFrame.isSelected()){
            return jrbDocterOptionsFrame.getText().toUpperCase();
        }
        else
        {
            return null;
        }
    
    }

