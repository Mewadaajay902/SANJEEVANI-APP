    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
boolean isValidInput=validateInput();

if(isValidInput==false)
{
    JOptionPane.showMessageDialog(null, "userid or password cannot be left empty!","Error",JOptionPane.ERROR_MESSAGE);
return;
}
String userType=getUserType();
if(userType==null){
    JOptionPane.showMessageDialog(null,"please choose an option","Error",JOptionPane.ERROR_MESSAGE);
return;
}
try
{
    User user=new User();
    user.setUserId(userId);
    user.setPassword(password);
    user.setUserType(userType);
    String username=UserDao.validateUser(user);
    if(username!=null)
    {
        JOptionPane.showMessageDialog(null,"Login Accepted !","Welcome "+username,JOptionPane.INFORMATION_MESSAGE);
        UserProfile.setUsername(username);
        UserProfile.setUsertype(userType);
        
        if(userType.equalsIgnoreCase("admin"))
        {
            AdminOptionsFrame adminOptionsFrame=new AdminOptionsFrame();
            adminOptionsFrame.setVisible(true);
        }
        else if(userType.equalsIgnoreCase("receptionist"))
        {
            PatientOptionsFrame Frame=new PatientOptionsFrame();
            Frame.setVisible(true);
        }
        else
        {
            DocterOptionsFrame docterOptionsFrame=new DocterOptionsFrame();
            docterOptionsFrame.setVisible(true);
        }
        this.dispose();
        
    }
    else
    {
        JOptionPane.showMessageDialog(null, "Invalidate Userid Password","Login denied",JOptionPane.ERROR_MESSAGE);
    }
}
    catch(Exception ex)
            {
            JOptionPane.showMessageDialog(null,"DBError"+ex,"Exception",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
            }
    
    



    }//GEN-LAST:event_jButton1ActionPerformed

